#include "libft.h"

char	*strrchr(const char *s, int c)
{
	char	*temp;

	temp = NULL;
	if (c == '\0')
	{
		while (*s)
			s++;
		return ((char *)s);
	}
	while (*s)
	{
		if (*s == c)
			temp = (char *)s;
	s++;
	}
	return (temp);
}
